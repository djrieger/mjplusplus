struct token {
    enum token_type type;
    std::string string_value;
};


int next_token(FILE *f, struct token *t) {

    t->string_value = "";

    int state = STATE_START;

    while(1) {

        int c = fgetc(f);
        int new_state = transitions[state][c == EOF ? 128 : c];

        if (!is_accepting(new_state))
            t->string_value = "";
        else if (new_state == STATE_STOP) {
            if (is_accepting(state)) {
                ungetc(c, f);
                if (keywords.find(t->string_value) != keywords.end())
                    t->type = TOKEN_KEYWORD;
                return 0;
            }
            else
                return -1;
        }
        else {
            t->string_value.push_back(c);
            t->type = state_type[new_state];
        }

        state = new_state;
    }

    return -1;
}


void print_token(struct token *t) {

    switch (t->type) {
        case TOKEN_EOF:
            std::cout << "EOF" << std::endl;
            break;
        case TOKEN_INT_LIT:
            std::cout << "integer literal " << t->string_value << std::endl;
            break;
        case TOKEN_IDENT:
            std::cout << "identifier " << t->string_value << std::endl;
            break;
        case TOKEN_OPERATOR:
        case TOKEN_KEYWORD:
            std::cout << t->string_value << std::endl;
            break;
    }
}


int main(int argc, const char *argv[]) {

    if (argc == 3 && strncmp(argv[1], "--lextest", 10) == 0) {

        FILE *f = fopen(argv[2], "r");
        if (f == NULL) {
            fprintf(stderr, "Error reading file.\n");
            return EXIT_FAILURE;
        }

        struct token token;
        while (next_token(f, &token) == 0) {
            print_token(&token);
            if (token.type == TOKEN_EOF)
                break;
        }

        fclose(f);

        if (token.type != TOKEN_EOF) {
            fprintf(stderr, "Error: lexer failed.\n");
            return EXIT_FAILURE;
        }
    }
    else {
        fprintf(stderr, "Error.\n");
        return EXIT_FAILURE;
    }

    return EXIT_SUCCESS;
}
